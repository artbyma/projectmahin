import {ethers} from "ethers";
import {useWeb3React} from "./web3wallet/core";

export function useCurveContract() {
  const { library, active } = useWeb3React();
  if (!library || !active) {
    return null;
  }
  return new ethers.Contract(process.env.NEXT_PUBLIC_CURVE_ADDRESS, curveAbi, library);
}

